package LoopDemo;

public class doWhileLoopDemo {
    public static void main(String[] args)
    {
        System.out.println("Do while loop started");
        int a=1;
        do
        {
            System.out.println("Values are:"+a);
            a++;
        }
        while(a<10);

        System.out.println("Do while loop ends");

    }


}
